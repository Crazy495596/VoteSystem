package Model;

public class Audience {
	public Integer audience_id;
	public Integer audience_voteid;
	public String audience_ip;
	public Integer getAudience_id() {
		return audience_id;
	}
	public void setAudience_id(Integer audience_id) {
		this.audience_id = audience_id;
	}
	public Integer getAudience_voteid() {
		return audience_voteid;
	}
	public void setAudience_voteid(Integer audience_voteid) {
		this.audience_voteid = audience_voteid;
	}
	public String getAudience_ip() {
		return audience_ip;
	}
	public void setAudience_ip(String audience_ip) {
		this.audience_ip = audience_ip;
	}
	

}
